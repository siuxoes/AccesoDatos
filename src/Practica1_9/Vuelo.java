    private void InformaColumnas() throws Exception {
        Connection connection = getConnection();
        DatabaseMetaData dbmd=connection.getMetaData();
        ResultSet resultSet = dbmd.getTables(null, null, "%", null);
        String out = "";
        while (resultSet.next()) {
            ResultSet resultado = dbmd.getColumns(null,"gestiondevuelos", resultSet.getString(3), null);
            out += "\n" + resultSet.getString(3) + "\n";
            while (resultado.next())
            {
                out +="\n" + "NOMBRE COLUMNA: "+ resultado.getString("COLUMN_NAME")+ "\n";
                out +="TIPO COLUMNA: "+ resultado.getString("TYPE_NAME")+ "\n";
                out +="TAMAÑO: "+ resultado.getString("COLUMN_SIZE")+ "\n";
                out +="ES NULA: "+ resultado.getString("IS_NULLABLE")+ "\n";
            }
        }
         jTextArea1.setText(out);
    }
