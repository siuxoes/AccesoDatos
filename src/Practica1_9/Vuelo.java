    public void InformaBD(){
        String titulo = "INFORMACIÓN SOBRE LA BASE DE DATOS.";
        String nombre="", driver="", url="", usuario="", tabla="";
        try {
            Connection connection = getConnection();
            DatabaseMetaData dbmd=connection.getMetaData();
            nombre = "Nombre: " + dbmd.getDatabaseProductName();
            driver = "Driver: " + dbmd.getDriverName();
            url = "URL: " + dbmd.getURL();
            usuario = "Usuario: " + dbmd.getUserName();
            ResultSet resultSet = dbmd.getTables(null, null, "%", null);
            tabla = "Tablas: ";
            while (resultSet.next()) {
                tabla += "\n" + resultSet.getString(3);
            }
            
        } catch (Exception ex) {
            Logger.getLogger(Vuelo.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTextArea1.setText(titulo + "\n" + nombre + "\n" + driver + "\n" + url + "\n" + usuario + "\n" + tabla);
    }
    
